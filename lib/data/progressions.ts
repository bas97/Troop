export function getProgressionsForSkill(skillId: string): Progression[] {
  return PROGRESSIONS.filter(p => p.skill_id === skillId).sort((a, b) => a.level - b.level)
}

export function getProgressionById(id: string): Progression | undefined {
  return PROGRESSIONS.find(p => p.id === id)
}

export function getNextProgression(currentId: string): Progression | undefined {
  const current = getProgressionById(currentId)
  if (!current) return undefined
  return PROGRESSIONS.find(p => p.skill_id === current.skill_id && p.level === current.level + 1)
}

export function getPreviousProgression(currentId: string): Progression | undefined {
  const current = getProgressionById(currentId)
  if (!current) return undefined
  return PROGRESSIONS.find(p => p.skill_id === current.skill_id && p.level === current.level - 1)
}
